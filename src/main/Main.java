package main;

public class Main {
    public static void main(String[] args) {
        // Primitive tipler
        int yas = 21;
        double maas = 3.45;
        char sınıf = 'B';
        boolean mezunMu = false;

        // Reference tip
        String ad = "Ayşe";

        System.out.println("Ad: " + ad);
        System.out.println("Yaş: " + yas);
        System.out.println("Maaş: " + maas);
        System.out.println("Sınıf: " + sınıf);
        System.out.println("Mezun mu? " + mezunMu);
    }
}



